package com.takeuforward.dsa.linkedlist.pattern;

public class pattern1 {

    static void main() {
        int n = 5;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= 4; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
